     private void tampilbuku (){
       txtjudul.removeAllItems();
       txtjudul.addItem("-- PILIH --");
       try {
          String sql ="select * from buku";
          Statement stat=con.createStatement();
          res=stat.executeQuery(sql);
           while (res.next()) {
               String pen =res.getString("judul");
               txtjudul.addItem(pen);
           }
       } catch (Exception e) {
           JOptionPane.showMessageDialog(null,"TAMPIL BUKU GAGAL!! \n" +e.getMessage());
       }
   }
     
