    private void ubahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ubahActionPerformed
        int ok=JOptionPane.showConfirmDialog(null,"Apakah yakin ingin mengedit data?","Confirmation",
                JOptionPane.YES_NO_OPTION);
        idbeli=txtbeli.getText();
        namabeli=txtnama.getText();
        alamatbeli=txtalamat.getText();
        teleponbeli=txttelp.getText();
        tglbeli=txttgl.getText();
        jdulbeli=txtjudul.getItemAt(txtjudul.getSelectedIndex()).toString();
        hrgabeli=txtharga.getText();
        pnulisbeli=txtpenulis.getItemAt(txtpenulis.getSelectedIndex()).toString();
        idpenbeli=txtidpen.getText();
        try {
            String sql=" update pembelian "
            + "set nama_pembeli='"+ namabeli +"',"
            + "alamat='"+ alamatbeli +"',"
            + "telepon='"+ teleponbeli +"',"
            + "tanggal_beli='"+ tglbeli +"',"
            + "judul='"+ jdulbeli +"',"
            + "harga='"+ hrgabeli +"',"
            + "penulis='"+ pnulisbeli +"',"
            + "id_penulis='"+ idpenbeli
            + "' where id_pembelian='"+ idbeli +"'";
            stat=con.createStatement();
            stat.execute(sql);
            new Pembelian().setVisible(true);
            dispose();
            JOptionPane.showMessageDialog(null,"BERHASIL EDIT DATA!!");
        } catch (Exception e) {
            JOptionPane.showConfirmDialog(null, "GAGAL EDIT DATA \n"
                +e.getMessage());
        }
        kosongkan();
    }//GEN-LAST:event_ubahActionPerformed

