    private void idotomatis()
    {
       try {
            String sql="select * from buku order by id_buku desc";
            stat=con.createStatement();
            res=stat.executeQuery(sql);
            if (res.next()) {
                String num = res.getString("id_buku").substring(1);
                String rm = "" + (Integer.parseInt(num) + 1);
                String il = "";

              txtbuku.setText("B" + il + rm);
            } else {
               txtbuku.setText("B1");
            }

           }catch(Exception e){
           JOptionPane.showMessageDialog(null, e);
           }
     }
    
     private void tampilpenulis (){
       txtpenulis.removeAllItems();
       txtpenulis.addItem("-- PILIH --");
       try {
          String sql ="select * from penulis";
          Statement stat=con.createStatement();
          res=stat.executeQuery(sql);
           while (res.next()) {
               String pen =res.getString("nama_penulis");
               txtpenulis.addItem(pen);
           }
       } catch (Exception e) {
           JOptionPane.showMessageDialog(null,"TAMPIL PENULIS GAGAL!! \n" +e.getMessage());
       }
   }

