    private void ubahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ubahActionPerformed
        int ok=JOptionPane.showConfirmDialog(null,"Apakah yakin ingin mengedit data?","Confirmation",
                JOptionPane.YES_NO_OPTION);
        idbuk=txtbuku.getText();
        judulbuk=txtjudul.getText();
        penulisbuk=txtpenulis.getItemAt(txtpenulis.getSelectedIndex()).toString();;
        halamanbuk=txthalaman.getText();
        penerbitbuk=txtpenerbit.getText();;
        hargabuk=txtharga.getText();
        try {
            String sql=" update  buku "
            + "set judul='"+ judulbuk +"',"
            + "penulis='"+ penulisbuk +"',"
            + "jumlah_halaman='"+ halamanbuk +"',"
            + "penerbit='"+ penerbitbuk +"',"
            + "harga='"+ hargabuk
            + "' where id_buku='"+ idbuk +"'";
            stat=con.createStatement();
            stat.execute(sql);
            new Buku().setVisible(true);
            dispose();
            JOptionPane.showMessageDialog(null,"BERHASIL EDIT DATA!!");
        } catch (Exception e) {
            JOptionPane.showConfirmDialog(null, "GAGAL EDIT DATA!! \n"
                +e.getMessage());
        }
        kosongkan();
    }//GEN-LAST:event_ubahActionPerformed

